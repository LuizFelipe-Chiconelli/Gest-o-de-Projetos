<h2 class="mb-4 text-primary">Produtos e Serviços</h2>

<p>Oferecemos uma plataforma completa para a <strong>gestão de projetos acadêmicos</strong>, com recursos que otimizam o trabalho de alunos e professores durante todo o ciclo de desenvolvimento de atividades. Confira os principais serviços disponíveis:</p>

<ul class="list-group list-group-flush my-3">
  <li class="list-group-item">
    <strong>✔️ Gerenciamento de Projetos:</strong> Criação, acompanhamento e atualização de projetos acadêmicos em tempo real.
  </li>
  <li class="list-group-item">
    <strong>✔️ Controle de Entregas:</strong> Envio e registro das atividades dentro dos prazos estipulados.
  </li>
  <li class="list-group-item">
    <strong>✔️ Reuniões Agendadas:</strong> Planejamento e visualização de reuniões entre alunos e orientadores.
  </li>
  <li class="list-group-item">
    <strong>✔️ Gestão de Usuários:</strong> Controle de acesso para administradores, professores e alunos, com permissões específicas para cada perfil.
  </li>
  <li class="list-group-item">
    <strong>✔️ Relatórios e Acompanhamentos:</strong> Visualização de progresso por projeto, aluno ou equipe.
  </li>
</ul>

<p>Nosso objetivo é transformar a forma como os projetos são gerenciados, tornando o processo mais ágil, claro e eficiente.</p>
