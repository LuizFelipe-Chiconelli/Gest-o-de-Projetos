<h2 class="mb-4 text-primary">Quem Somos</h2>
<p>
  Somos uma equipe comprometida com a inovação e a melhoria contínua no ambiente acadêmico. Nosso sistema foi desenvolvido com o objetivo de facilitar a <strong>gestão de projetos educacionais</strong>, promovendo a organização, transparência e eficiência no acompanhamento de todas as etapas do desenvolvimento de trabalhos e atividades dos alunos.
</p>

<p>
  Acreditamos que a tecnologia deve ser uma aliada na educação, e por isso criamos uma plataforma moderna, intuitiva e segura, pensada para atender as necessidades de <strong>alunos, professores e coordenadores</strong>. Aqui, o foco é a colaboração e o progresso conjunto, com funcionalidades que simplificam o dia a dia acadêmico.
</p>

<p>
  Juntos, construímos pontes entre conhecimento e prática, transformando ideias em projetos de sucesso.
</p>
